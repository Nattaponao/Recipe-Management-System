import { AIRecipeInput } from '@/types/ai';

export function buildAnalyzePrompt(
  recipes: AIRecipeInput[],
  userIngredients: string[],
) {
  return `
คุณคือผู้เชี่ยวชาญด้านอาหารไทยและนานาชาติ วิเคราะห์สูตรอาหารโดยเปรียบเทียบวัตถุดิบที่มีกับสิ่งที่สูตรต้องการ

รายการสูตรอาหารในระบบ: ${JSON.stringify(recipes)}
วัตถุดิบที่ผู้ใช้มีในขณะนี้: ${userIngredients.join(', ')}

กฎสำคัญในการจับคู่วัตถุดิบ:
ให้ถือว่าจับคู่ได้เมื่อวัตถุดิบมีความหมายเดียวกัน ใกล้เคียงกัน หรือทดแทนกันได้ในการปรุงอาหาร เช่น
- กั้ง = กุ้ง = กุ้งแม่น้ำ = กุ้งทะเล (สัตว์น้ำประเภทเดียวกัน ใช้แทนกันได้)
- ปลาหมึก = หมึก = หมึกกล้วย = หมึกสาย
- หมูสับ = เนื้อหมูบด = หมูบด
- ไก่ = เนื้อไก่ = อกไก่ = น่องไก่
- ซีอิ๊ว = ซีอิ๊วขาว = ซอสถั่วเหลือง
- พริก = พริกแดง = พริกเขียว = พริกสด = พริกขี้หนู
- กระเทียม = กระเทียมจีน = กระเทียมไทย
- ต้นหอม = หอมต้น = หอมแบ่ง
- มะนาว = เลมอน

หลักการเพิ่มเติม:
- พิจารณาความหมายเชิงอาหาร ไม่ใช่แค่ตัวอักษรที่ตรงกัน
- ถ้าวัตถุดิบของผู้ใช้ทดแทนได้ ให้นับว่ามี ไม่ใช่ขาด
- ถ้าวัตถุดิบในสูตรเป็นเครื่องปรุงพื้นฐาน เช่น เกลือ น้ำตาล น้ำมัน ให้ถือว่ามีเสมอ

งานของคุณ:
1. เปรียบเทียบวัตถุดิบของผู้ใช้กับ ingredients ของแต่ละสูตรโดยใช้กฎจับคู่ข้างต้น
2. ตรวจสอบใน steps เพิ่มเติมว่ามีวัตถุดิบสำคัญที่ขาดหรือไม่
3. missingIngredients ระบุเฉพาะวัตถุดิบที่ผู้ใช้ไม่มีและหาทดแทนไม่ได้จริงๆ เท่านั้น
4. matchScore คำนวณเปอร์เซ็นต์ความเข้ากัน 0-100 นับทั้งที่ตรงและที่ทดแทนกันได้
5. reason อธิบายเฉพาะเจาะจงว่าวัตถุดิบไหนตรง วัตถุดิบไหนทดแทน และขาดอะไรบ้าง ห้ามใช้ข้อความกว้างๆ ซ้ำทุกเมนู
`;
}
