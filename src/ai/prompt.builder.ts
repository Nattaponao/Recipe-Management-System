export type RecommendContext = {
  userRequest: string;
  count?: number;
};

export function buildRecommendPrompt(context: RecommendContext): string {
  const { userRequest, count = 5 } = context;

  return `
คุณคือเชฟและนักโภชนาการผู้เชี่ยวชาญอาหารนานาชาติ

ความต้องการของผู้ใช้: "${userRequest}"

งานแรก: อ่านข้อความข้างต้นแล้วดึงข้อมูลดังนี้ออกมาในใจก่อน
- ต้องการอาหารแบบไหน? (รสชาติ, ประเทศ, วัตถุดิบ, ฯลฯ)
- มีโรคประจำตัวหรือข้อจำกัดอาหารระบุไว้ไหม? เช่น เบาหวาน, ความดัน, แพ้กลูเตน, มังสวิรัติ

งานหลัก: แนะนำเมนูอาหาร ${count} เมนู พร้อมสูตรทำอาหารครบถ้วน

กฎสำคัญด้านสุขภาพ (ใช้เมื่อผู้ใช้ระบุโรคประจำตัว):
- เบาหวาน: ลดน้ำตาล แป้งขัดขาว อาหาร GI สูง ใช้เครื่องเทศแทนความหวาน
- ความดันสูง: ลดโซเดียม หลีกเลี่ยงอาหารแปรรูปและเค็มจัด
- ไขมันในเลือดสูง: ลดไขมันอิ่มตัว หลีกเลี่ยงของทอด
- แพ้กลูเตน: ไม่ใช้แป้งสาลี ใช้แป้งข้าวเจ้าหรือแป้งมันแทน
- มังสวิรัติ: ไม่มีเนื้อสัตว์ทุกชนิด

แต่ละเมนูต้องมีข้อมูลครบดังนี้:
- recipeName: ชื่อเมนูภาษาไทย
- originalName: ชื่อในภาษาต้นทาง (ถ้ามี)
- cuisine: ประเทศหรือวัฒนธรรมอาหาร
- description: อธิบายเมนูสั้นๆ 1-2 ประโยค
- spiceLevel: mild / medium / hot / very_hot
- healthNote: อธิบายว่าเมนูนี้เหมาะกับสุขภาพของผู้ใช้อย่างไร หรือควรระวังอะไร
- servings: จำนวนที่รับประทานได้ เช่น "2 คน"
- prepTime: เวลาเตรียม เช่น "15 นาที"
- cookTime: เวลาทำ เช่น "20 นาที"
- ingredients: array ของ { name, amount }
- steps: array ของ { stepNumber, instruction } เรียงตามลำดับขั้นตอน

ตอบเป็น JSON array เท่านั้น ห้ามมี text อื่นนอกจาก JSON
`.trim();
}
